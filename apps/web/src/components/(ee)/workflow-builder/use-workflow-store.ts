/**
 * Convert workflow steps to React Flow nodes.
 * Collapses cascade primitive steps (identified by cascadeGroupId) back into
 * single cascade nodes using the stored cascadeChannels metadata.
 */
function stepsToNodes(steps: WorkflowStep[]): WorkflowNode[] {
  // Identify cascade groups from steps
  const cascadeGroupIds = new Set<string>();
  for (const step of steps) {
    if (step.cascadeGroupId) {
      cascadeGroupIds.add(step.cascadeGroupId);
    }
  }

  // If no cascade groups, return flat nodes
  if (cascadeGroupIds.size === 0) {
    return steps.map((step) => ({
      id: step.id,
      type: step.type,
      position: step.position,
      data: {
        stepId: step.id,
        type: step.type,
        name: step.name,
        config: step.config,
        isValid: true,
      },
    }));
  }

  const allNodes: WorkflowNode[] = [];

  // Process each cascade group → collapse into a single cascade node
  for (const groupId of cascadeGroupIds) {
    const groupSteps = steps.filter((s) => s.cascadeGroupId === groupId);
    if (groupSteps.length === 0) continue;

    // Look for cascadeChannels metadata on the first step
    const stepWithMeta = groupSteps.find((s) => s.cascadeChannels);
    const channels: CascadeChannelConfig[] =
      stepWithMeta?.cascadeChannels ??
      reconstructChannelsFromPrimitives(groupSteps);

    // Use the first step's position for the cascade node
    const firstStep = groupSteps.reduce((a, b) =>
      a.position.y < b.position.y ? a : b
    );

    const cascadeNode: WorkflowNode = {
      id: groupId,
      type: "cascade",
      position: firstStep.position,
      data: {
        stepId: groupId,
        type: "cascade",
        name: "Cascade",
        config: { type: "exit" } as WorkflowStepConfig, // placeholder
        isValid: true,
        cascadeChannels: channels,
      },
    };
    allNodes.push(cascadeNode);
  }

  // Add non-cascade nodes
  for (const step of steps) {
    if (!step.cascadeGroupId) {
      allNodes.push({
        id: step.id,
        type: step.type,
        position: step.position,
        data: {
          stepId: step.id,
          type: step.type,
          name: step.name,
          config: step.config,
          isValid: true,
        },
      });
    }
  }

  return allNodes;
}

/**
 * Reconstruct cascade channels from primitive steps when cascadeChannels
 * metadata is not available (backward compatibility with old data).
 */
function reconstructChannelsFromPrimitives(
  groupSteps: WorkflowStep[]
): CascadeChannelConfig[] {
  const channels: CascadeChannelConfig[] = [];

  // Find send steps (these represent channels)
  const sendSteps = groupSteps.filter(
    (s) => s.type === "send_email" || s.type === "send_sms"
  );

  // Find wait steps for engagement duration
  const waitSteps = groupSteps.filter(
    (s) => s.type === "wait_for_email_engagement"
  );

  for (let i = 0; i < sendSteps.length; i++) {
    const sendStep = sendSteps[i];
    const isLast = i === sendSteps.length - 1;

    if (sendStep.type === "send_email") {
      const templateId =
        sendStep.config.type === "send_email" ? sendStep.config.templateId : "";
      // Find the corresponding wait step (by position order)
      const waitStep = waitSteps[i] || waitSteps[0];
      const waitDuration =
        waitStep?.config.type === "wait_for_email_engagement"
          ? waitStep.config.timeoutSeconds
          : undefined;

      channels.push({
        type: "email",
        templateId: templateId || undefined,
        engagement: "opened",
        waitDuration: isLast ? undefined : (waitDuration ?? 259_200),
      });
    } else if (sendStep.type === "send_sms") {
      const body =
        sendStep.config.type === "send_sms" ? sendStep.config.body : undefined;
      channels.push({
        type: "sms",
        body: body || undefined,
      });
    }
  }

  // Fallback: if no channels found, return default
  if (channels.length === 0) {
    return [
      {
        type: "email",
        engagement: "opened",
        waitDuration: 259_200,
      },
      { type: "sms" },
    ];
  }

  return channels;
}

/**
 * Convert workflow transitions to React Flow edges.
 * Filters out internal cascade transitions (those between primitives in a group)
 * and maps external transitions to/from cascade nodes.
 */
function transitionsToEdges(
  transitions: WorkflowTransition[],
  steps: WorkflowStep[]
): WorkflowEdge[] {
  // Build cascade group membership map
  const stepToGroup = new Map<string, string>();
  const groupFirstStep = new Map<string, string>();
  const groupEngagedExits = new Map<string, string[]>();
  const groupLastSend = new Map<string, string>();

  for (const step of steps) {
    if (step.cascadeGroupId) {
      stepToGroup.set(step.id, step.cascadeGroupId);
    }
  }

  // Identify first send, engaged exits, and last send per group
  for (const groupId of new Set(stepToGroup.values())) {
    const groupSteps = steps.filter((s) => s.cascadeGroupId === groupId);
    const sendSteps = groupSteps.filter(
      (s) => s.type === "send_email" || s.type === "send_sms"
    );
    const exitSteps = groupSteps.filter((s) => s.type === "exit");

    if (sendSteps.length > 0) {
      // First send step (entry point)
      const firstSend = sendSteps.reduce((a, b) =>
        a.position.y < b.position.y ? a : b
      );
      groupFirstStep.set(groupId, firstSend.id);

      // Last send step (exhausted endpoint)
      const lastSend = sendSteps.reduce((a, b) =>
        a.position.y > b.position.y ? a : b
      );
      groupLastSend.set(groupId, lastSend.id);
    }

    // Exit steps represent "engaged" exits
    if (exitSteps.length > 0) {
      groupEngagedExits.set(
        groupId,
        exitSteps.map((s) => s.id)
      );
    }
  }

  const edges: WorkflowEdge[] = [];

  for (const transition of transitions) {
    const fromGroup = stepToGroup.get(transition.fromStepId);
    const toGroup = stepToGroup.get(transition.toStepId);

    // Skip purely internal cascade transitions
    if (fromGroup && toGroup && fromGroup === toGroup) {
      continue;
    }

    // Map legacy "none" branch to "timeout" handle ID
    let branch = transition.condition?.branch as string | undefined;
    if (branch === "none") branch = "timeout";

    let source = transition.fromStepId;
    let target = transition.toStepId;
    let sourceHandle = branch;

    // Map from cascade primitive → cascade node handle
    if (fromGroup) {
      source = fromGroup;
      const engagedExits = groupEngagedExits.get(fromGroup) || [];
      if (engagedExits.includes(transition.fromStepId)) {
        // Edge from an engaged exit → map to "engaged" handle
        sourceHandle = "engaged";
      } else if (transition.fromStepId === groupLastSend.get(fromGroup)) {
        // Edge from last send → map to "exhausted" handle
        sourceHandle = "exhausted";
      }
    }

    // Map to cascade primitive → cascade node
    if (toGroup) {
      target = toGroup;
    }

    edges.push({
      id: transition.id,
      source,
      target,
      sourceHandle,
      data: { label: sourceHandle },
    });
  }

  return edges;
}

/**
 * Expand a cascade node into primitive workflow steps and transitions.
 */
function expandCascadeNode(node: WorkflowNode): {
  steps: WorkflowStep[];
  transitions: WorkflowTransition[];
  /** ID of the first send step (entry point for incoming edges) */
  firstStepId: string;
  /** IDs of condition steps whose "yes" branch = "engaged" */
  engagedConditionIds: string[];
  /** ID of the last send step (source for "exhausted" edges) */
  lastSendId: string;
  /** ID of the "engaged exit" node, if one was created */
  engagedExitId: string;
} {
  const channels = node.data.cascadeChannels ?? [];
  const cascadeGroupId = node.id;
  const steps: WorkflowStep[] = [];
  const transitions: WorkflowTransition[] = [];
  const engagedConditionIds: string[] = [];
  let firstStepId = "";
  let lastSendId = "";
  let engagedExitId = "";

  // Track the previous step ID and whether it was a condition step
  let prevStepId = "";
  let prevIsCondition = false;

  for (let i = 0; i < channels.length; i++) {
    const channel = channels[i];
    const isLast = i === channels.length - 1;
    const isFirst = i === 0;

    // Create send step with deterministic ID
    const sendId = `${cascadeGroupId}-send-${i}`;
    const sendStep: WorkflowStep = {
      id: sendId,
      type: channel.type === "email" ? "send_email" : "send_sms",
      name:
        channel.type === "email"
          ? `Send Email ${channels.filter((c, j) => c.type === "email" && j <= i).length}`
          : `Send SMS ${channels.filter((c, j) => c.type === "sms" && j <= i).length}`,
      position: { x: node.position.x, y: node.position.y + i * 200 },
      config:
        channel.type === "email"
          ? ({
              type: "send_email",
              templateId: channel.templateId || "",
            } as WorkflowStepConfig)
          : ({
              type: "send_sms",
              body: channel.body,
            } as WorkflowStepConfig),
      cascadeGroupId,
      // Store cascade metadata on the first step for reconstruction
      ...(isFirst ? { cascadeChannels: channels } : {}),
    };
    steps.push(sendStep);

    if (isFirst) firstStepId = sendId;
    lastSendId = sendId;

    // Chain from previous step
    if (prevStepId) {
      transitions.push({
        id: `${cascadeGroupId}-tr-${prevStepId}-${sendId}`,
        fromStepId: prevStepId,
        toStepId: sendId,
        // Only condition steps use branch-based transitions ("no" = not engaged).
        // SMS send steps use branchless transitions so the backend's
        // processNextStep fallback routing (`!t.condition`) picks them up.
        condition: prevIsCondition ? { branch: "no" } : undefined,
      });
    }

    // If not last channel and email type, add wait + condition
    if (!isLast && channel.type === "email" && channel.waitDuration) {
      const waitId = `${cascadeGroupId}-wait-${i}`;
      const conditionId = `${cascadeGroupId}-cond-${i}`;

      // Wait for engagement step
      steps.push({
        id: waitId,
        type: "wait_for_email_engagement",
        name: "Wait for Engagement",
        position: {
          x: node.position.x,
          y: node.position.y + i * 200 + 80,
        },
        config: {
          type: "wait_for_email_engagement",
          timeoutSeconds: channel.waitDuration,
        } as WorkflowStepConfig,
        cascadeGroupId,
      });

      // Condition step
      steps.push({
        id: conditionId,
        type: "condition",
        name: "Engaged?",
        position: {
          x: node.position.x,
          y: node.position.y + i * 200 + 140,
        },
        config: {
          type: "condition",
          field: CASCADE_ENGAGEMENT_FIELD,
          operator: "equals",
          value: "true",
        } as WorkflowStepConfig,
        cascadeGroupId,
      });

      // send → wait
      transitions.push({
        id: `${cascadeGroupId}-tr-${sendId}-${waitId}`,
        fromStepId: sendId,
        toStepId: waitId,
      });

      // wait → condition (timeout)
      transitions.push({
        id: `${cascadeGroupId}-tr-${waitId}-${conditionId}`,
        fromStepId: waitId,
        toStepId: conditionId,
        condition: { branch: "timeout" },
      });

      engagedConditionIds.push(conditionId);
      prevStepId = conditionId;
      prevIsCondition = true;
    } else if (!isLast) {
      // Non-email channel (SMS) in non-last position: direct chain to next
      prevStepId = sendId;
      prevIsCondition = false;
    }
  }

  // Create a single "engaged exit" node for all conditions' "yes" branches
  if (engagedConditionIds.length > 0) {
    const exitId = `${cascadeGroupId}-exit`;
    engagedExitId = exitId;
    steps.push({
      id: exitId,
      type: "exit",
      name: "Engaged Exit",
      position: {
        x: node.position.x + 250,
        y: node.position.y + 100,
      },
      config: {
        type: "exit",
        reason: "Engaged via cascade",
      } as WorkflowStepConfig,
      cascadeGroupId,
    });

    // Connect all conditions' "yes" to the exit
    for (const condId of engagedConditionIds) {
      transitions.push({
        id: `${cascadeGroupId}-tr-${condId}-${exitId}`,
        fromStepId: condId,
        toStepId: exitId,
        condition: { branch: "yes" },
      });
    }
  }

  return {
    steps,
    transitions,
    firstStepId,
    engagedConditionIds,
    lastSendId,
    engagedExitId,
  };
}

/**
 * Convert React Flow nodes to workflow steps.
 * Expands cascade nodes into primitive steps.
 */
function nodesToSteps(nodes: WorkflowNode[]): {
  steps: WorkflowStep[];
  internalTransitions: WorkflowTransition[];
  cascadeMappings: Map<string, CascadeMapping>;
} {
  const steps: WorkflowStep[] = [];
  const internalTransitions: WorkflowTransition[] = [];
  const cascadeMappings = new Map<string, CascadeMapping>();

  for (const node of nodes) {
    if (node.type === "cascade") {
      const expanded = expandCascadeNode(node);
      steps.push(...expanded.steps);
      internalTransitions.push(...expanded.transitions);
      cascadeMappings.set(node.id, {
        firstStepId: expanded.firstStepId,
        engagedConditionIds: expanded.engagedConditionIds,
        lastSendId: expanded.lastSendId,
        engagedExitId: expanded.engagedExitId,
        allStepIds: expanded.steps.map((s) => s.id),
      });
    } else {
      steps.push({
        id: node.id,
        type: node.data.type as WorkflowStepType,
        name: node.data.name,
        position: node.position,
        config: node.data.config,
      });
    }
  }

  return { steps, internalTransitions, cascadeMappings };
}

/**
 * Convert React Flow edges to workflow transitions.
 * Resolves cascade node handles to their primitive step IDs.
 */
function edgesToTransitions(
  edges: WorkflowEdge[],
  cascadeMappings: Map<string, CascadeMapping>
): {
  transitions: WorkflowTransition[];
  /** Cascade node IDs that have externally-routed engaged edges */
  engagedExternalCascades: Set<string>;
} {
  const transitions: WorkflowTransition[] = [];
  // Track which cascade nodes have externally-routed engaged edges
  const engagedExternalTargets = new Map<string, string>();

  for (const edge of edges) {
    let fromStepId = edge.source;
    let toStepId = edge.target;
    let branch = edge.sourceHandle as string | undefined;

    // Resolve cascade target → first send step
    const targetMapping = cascadeMappings.get(edge.target);
    if (targetMapping) {
      toStepId = targetMapping.firstStepId;
    }

    // Resolve cascade source → primitive step based on handle
    const sourceMapping = cascadeMappings.get(edge.source);
    if (sourceMapping) {
      if (edge.sourceHandle === "engaged") {
        // "Engaged" means the contact opened/clicked an email in the cascade.
        // Create a transition from each engaged condition's "yes" to the target,
        // replacing the internal exit node connections.
        for (const condId of sourceMapping.engagedConditionIds) {
          transitions.push({
            id: crypto.randomUUID(),
            fromStepId: condId,
            toStepId,
            condition: { branch: "yes" },
          });
        }
        // Mark that engaged edges are externally routed so we can
        // remove the internal exit transitions later
        engagedExternalTargets.set(edge.source, toStepId);
        continue;
      }
      if (edge.sourceHandle === "exhausted") {
        fromStepId = sourceMapping.lastSendId;
        branch = undefined; // default connection from last send
      }
    }

    transitions.push({
      id: edge.id,
      fromStepId,
      toStepId,
      condition: branch
        ? {
            branch: branch as
              | "yes"
              | "no"
              | "timeout"
              | "default"
              | "opened"
              | "clicked"
              | "bounced",
          }
        : undefined,
    });
  }

  return {
    transitions,
    engagedExternalCascades: new Set(engagedExternalTargets.keys()),
  };
}

/**
 * Remove internal condition→exit transitions for cascades with external engaged edges.
 * When a user connects the "engaged" output to another node, we route directly
 * from condition "yes" branches to that node — so the internal exit transitions
 * should be removed to avoid duplicate routing.
 */
function filterInternalTransitions(
  internalTransitions: WorkflowTransition[],
  cascadeMappings: Map<string, CascadeMapping>,
  engagedExternalCascades: Set<string>
): WorkflowTransition[] {
  if (engagedExternalCascades.size === 0) return internalTransitions;

  // Collect exit step IDs for externally-routed cascades
  const exitStepIds = new Set<string>();
  for (const cascadeId of engagedExternalCascades) {
    const mapping = cascadeMappings.get(cascadeId);
    if (mapping?.engagedExitId) {
      exitStepIds.add(mapping.engagedExitId);
    }
  }

  // Remove transitions that target these exit steps
  return internalTransitions.filter((t) => !exitStepIds.has(t.toStepId));
}

/**
 * Remove internal "engaged exit" steps for cascades with external engaged edges.
 * The exit node is no longer needed when the engaged path routes externally.
 */
function filterEngagedExitSteps(
  steps: WorkflowStep[],
  cascadeMappings: Map<string, CascadeMapping>,
  engagedExternalCascades: Set<string>
): WorkflowStep[] {
  if (engagedExternalCascades.size === 0) return steps;

  const exitStepIds = new Set<string>();
  for (const cascadeId of engagedExternalCascades) {
    const mapping = cascadeMappings.get(cascadeId);
    if (mapping?.engagedExitId) {
      exitStepIds.add(mapping.engagedExitId);
    }
  }

  return steps.filter((s) => !exitStepIds.has(s.id));
}

/**
 * Get default config for a step type
 */
function getDefaultConfig(type: WorkflowStepType): WorkflowStepConfig {
  switch (type) {
    case "trigger":
      return { type: "trigger", triggerType: "event" };
    case "send_email":
      return { type: "send_email", templateId: "" };
    case "send_sms":
      return { type: "send_sms" };
    case "delay":
      return { type: "delay", amount: 1, unit: "days" };
    case "exit":
      return { type: "exit" };
    case "condition":
      return { type: "condition", field: "", operator: "equals", value: "" };
    case "webhook":
      return { type: "webhook", url: "", method: "POST" };
    case "update_contact":
      return { type: "update_contact", updates: [] };
    case "wait_for_event":
      return { type: "wait_for_event", eventName: "" };
    case "wait_for_email_engagement":
      return { type: "wait_for_email_engagement", timeoutSeconds: 259_200 }; // 3 days default
    case "subscribe_topic":
      return { type: "subscribe_topic", topicId: "", channel: "email" };
    case "unsubscribe_topic":
      return { type: "unsubscribe_topic", topicId: "", channel: "email" };
  }
}

